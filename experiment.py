'''Fibonacci'''
def fib(number:int):
    if number <= 1:
        return 1;

    return fib(number-1) + fib(number-2)


'''Next Permutation'''
def next_permutation(a:list[int]) -> list[int]:
    """
    Generates the next permutation of a given list a.

    The next permutation is generated by iterating through the list in reverse order,
    finding the rightmost element which is smaller than the next element (pivot value),
    swapping the pivot value with the rightmost element which is bigger than it,
    and then reversing the subset of next value after the pivot value until the list end.
    If no such pivot value can be found, the list is in descending order and is reversed.

    :param a: list to generate the next permutation from
    :return: the next permutation of list a
    """
    for i in reversed(range(len(a))):
        if i == 0:
            return a[::-1]
        
        elif (a[i-1] < a[i]):
            pivot = i-1

            for j in reversed(range(pivot+1, len(a))):
                if(a[j] > a[pivot]):
                    
                    temp_pivot = a[pivot]

                    a[pivot] = a[j]
                    a[j] = temp_pivot

                    b = list(reversed(a[pivot+1:]))

                    return a[:pivot+1] + b




'''Longest Valid Parentheses'''
def longest_valid_parantheses(a:str) -> int:
    """
    Generates the length of the longest valid parentheses substring in a given string a.

    The longest valid parentheses substring is generated by iterating through the string,
    counting the number of valid parentheses, and keeping track of the longest valid substring.

    :param a: string to find the longest valid parentheses substring in
    :return: the length of the longest valid parentheses substring in string a
    """
    open_start = False
    valid_count = 0
    for i in range(len(a)):
        if a[i] == '(':
            open_start = True

        elif a[i] == ')' and open_start == True:
            valid_count += 2 
            open_start = False
        
    return valid_count




'''Hour Glass Sum'''
def hourGlassSum(a:list) -> int:
    """
    Generates the maximum sum of an hour glass shape in a given 2D list a.

    The hour glass shape is generated by iterating through the list, 
    calculating the sum of an hour glass shape at each position, 
    and keeping track of the maximum sum.

    :param a: 2D list to find the maximum hour glass sum in
    :return: the maximum hour glass sum in list a
    """
    sum_is= []
    for i in range(4):
        for j in range(4):
            sum_is.append(a[i][j] + a[i][j+1] + a[i][j+2] + a[i+1][j+1] + a[i+2][j] + a[i+2][j+1] + a[i+2][j+2])

    return max(sum_is)




'''Left Rotation Operation'''
def left_rotation(a:list[int], d:int) -> list:
    """
    Rotates a given list a to the left by d steps.

    The left rotation operation is performed by iterating through the list d times,
    shifting each element one position to the left, and moving the first element to the end of the list.

    :param a: list to rotate
    :param d: number of steps to rotate the list
    :return: rotated list
    """
    for _ in range(d):
        temp = a.pop(0)
        a.append(temp)
    
    return a





