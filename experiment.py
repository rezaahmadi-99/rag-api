'''A class to handle complex number operations'''
def sign_is(n):
    if n >= 0:
        return '+'
    else:
        return '-'

class Complex(object):
    def __init__(self, real, imaginary):        
        self.real = real
        self.imaginary = imaginary   
    def __add__(self, no):
        real_sum = self.real + no.real
        imaginary_sum = self.imaginary + no.imaginary
        result = f"{format(real_sum, ".2f")}{sign_is(imaginary_sum)}{format(abs(imaginary_sum), ".2f")}i"
        return result
    def __sub__(self, no):
        real_sub = self.real - no.real
        imaginary_sub = self.imaginary - no.imaginary
        result = f"{format(real_sub, ".2f")}{sign_is(imaginary_sub)}{format(abs(imaginary_sub), ".2f")}i"
        return result
    def __mul__(self, no):
        # (a+bi)*(c+di) = (ac-bd)+(ad+bc)i
        real_mul = (self.real * no.real) - (self.imaginary * no.imaginary)
        imaginary_mul = (self.real * no.imaginary) + (self.imaginary * no.real)
        result = f"{format(real_mul, ".2f")}{sign_is(imaginary_mul)}{format(abs(imaginary_mul), ".2f")}i"
        return result
    def __truediv__(self, no):
        # (a+bi)*(c-di) = (ac+bd)+(-ad+bc)i
        numerator_real = (self.real * no.real) + (self.imaginary * no.imaginary)
        numerator_imaginary = (-self.real * no.imaginary) + (self.imaginary * no.real)
        # (c+di)*(c-di) = c^2 + d^2
        denominator = no.real ** 2 + no.imaginary ** 2
        real = numerator_real / denominator
        imaginary = numerator_imaginary / denominator
        result = f"{format(real, ".2f")}{sign_is(imaginary)}{format(abs(imaginary), ".2f")}i"
        return result
         
    def mod(self):
        real = math.sqrt(self.real ** 2 + self.imaginary ** 2)
        result = f"{format(real, ".2f")}+0.00i"
        return result
    def __str__(self):
        if self.imaginary == 0:
            result = "%.2f+0.00i" % (self.real)
        elif self.real == 0:
            if self.imaginary >= 0:
                result = "0.00+%.2fi" % (self.imaginary)
            else:
                result = "0.00-%.2fi" % (abs(self.imaginary))
        elif self.imaginary > 0:
            result = "%.2f+%.2fi" % (self.real, self.imaginary)
        else:
            result = "%.2f-%.2fi" % (self.real, abs(self.imaginary))
        return result
    





